

# Ask for the user's name and store it in a variable called "name"
name = input("What's your name? ")

# Ask for the user's age and store it in a variable called "age"
age = int(input("How old are you? "))

# Ask for the user's location and store it in a variable called "location"
location = input("Where are you located? ")

# Print out a personalized message using the user's name, age, and location
print(f"Hello {name}, you are {age} years old and live in {location}.")
